# 6. Pythonのset（コンテナ型の1つ）について調べよう。setは何に使えるだろう？

"""
setの有効な使い方：集合を容易に扱える！

リストとの違い
・重複した要素がない
・要素に順番がない
"""

# set型の基本的な使い方

    #初期値入力
set1={4,4,5,6,7,6,7,5,5,7}
print(set1)

print("---------------------------------------------------------------")

    #文字列
set2=set("letters")
print(set2)

print("---------------------------------------------------------------")

    #リスト
set3=set([1,2,3])
list4=[1,2,3,3,2,1]
set4=set(list4)

print(set3)
print(set4)

print("---------------------------------------------------------------")

# 要素を追加

set4.add(4)
print(set4)

set2.add(3)
set2.add("a")
print(set2)

print("---------------------------------------------------------------")

# 要素を削除

set1.remove(5)
print(set1)

set2.remove("a")
print(set2)

print("---------------------------------------------------------------")
print("---------------------------------------------------------------")
print("---------------------------------------------------------------")

# 和集合「|」の計算

A={1,2,3}
B={3,4,5}

C=A|B
print(C)

print("---------------------------------------------------------------")

# 積集合「&」の計算

D=A&B
print(D)

print("---------------------------------------------------------------")

# 差集合「-」の計算

E=A-B
print(E)

print("---------------------------------------------------------------")

# 排他的論理和「^」の計算

F=A^B
print(F)
